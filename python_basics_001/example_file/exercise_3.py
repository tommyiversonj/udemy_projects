# Create a list of 4 float numbers, compute the average and print it 
orange_prices_list = [4.4, 5.5, 6.6, 7.7]

# Can use the function sum(list) to sum all elements in the list
total = sum(orange_prices_list)

# Reminder: average = sum of all elements / number of elements
average = total / len(orange_prices_list)

# Print the result
print("The average orange price is:", average)
