# Built in functions in Python are pre-defined functions that are readily available for use.
# These functions perform common tasks and can be used without needing to define them.
# Examples include functions like `print()`, `len()`, and `max()`.
# The following code demonstrates how to use a built-in function in Python.     

print(len("Hello, World!"))  # Output: 13
print(max(5, 3))  # Output: 5   
print(min(5, 3))  # Output: 3   
print(max)
print(sum([1, 2, 3, 4, 5]))  # Output: 15
print(sorted([5, 3, 1, 4, 2]))  # Output: [1, 2, 3, 4, 5]
print(range(5))  # Output: range(0, 5)