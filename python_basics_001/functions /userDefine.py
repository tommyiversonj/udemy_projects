# User defined functions in Python is a block of reusable code that performs a specific task. Functions help in organizing code, making it more modular and easier to maintain.
# Functions can take arguments, perform operations, and return values. They are defined using the `def` keyword followed by the function name and parentheses containing any parameters.
# The body of the function contains the code that will be executed when the function is called. Functions can also return values using the `return` statement, but this is optional.
# Below are some examples of defining and calling functions in Python, including how to pass arguments by value and by reference.       
# The following code demonstrates how to define and call a function in Python.

def add(a, b):
    """This function adds two numbers and returns the result."""
    return a + b
print(add(5, 3))  # Output: 8
print(max(5, 3))  # Output: 30