# A string is non-numeric data type that is used to represent text rather than numbers.
# Strings are enclosed in single quotes, double quotes, or triple quotes.   
# String is a sequence of characters enclosed in quotes.

str = 'Hello, World!'
print("String variable: ", str, type(str))
print(str) # Prints the complete string
print(str[0]) # Prints the first character of the string
print(str[2:5]) # Prints characters starting from index 2 to 4
print(str[2:]) # Prints characters starting from index 2 to the end
print(str * 2) # Prints the string two times
print(str + " How are you?") # Concatenates two strings
