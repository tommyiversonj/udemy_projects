# Boolean data type in Python is used to represent truth values.
# It can have two values: True or False.    

a = True  # Assigning True to variable a
print(a)  # Output: True
print(type(a))  # Output: <class 'bool'>, indicating that a is of boolean type

c = 2
d = 4

print(c < d)  # Output: True, because 2 is less than 4
print(c > d)  # Output: False, because 2 is not greater than 4
print(c == d)  # Output: False, because 2 is not equal to 4
print(c != d)  # Output: True, because 2 is not equal to 4
print(c <= d)  # Output: True, because 2 is less than or equal to 4
print(c >= d)  # Output: False, because 2 is not greater than or equal to 4