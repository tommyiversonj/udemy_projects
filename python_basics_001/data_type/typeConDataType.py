# Type conversion data type in Python is the process of converting one data type to another.
# This is useful when you need to perform operations on different data types or when you want to
# ensure that the data is in the correct format for a specific operation.       

print("Conversion to integer data type:")
a = int(1)  # Converting a float to an integer
b = int(2.2)  # Converting a float to an integer
c = int("3")  # Converting a string to an integer 

print(a)  # Output: 1
print(b)  # Output: 2
print(c)  # Output: 3, the string "3.3" is converted

