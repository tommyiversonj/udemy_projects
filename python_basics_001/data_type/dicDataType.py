# Dictionary Data Type in Python is a collection of key-value pairs.
# It is unordered, changeable, and indexed.
# Example of a dictionary
dict = {} # Creating an empty dictionary
dict['one'] = 'This is one' # Adding a key-value pair to the dictionary
dict[2] = "This is two" # Adding another key-value pair to the dictionary

tinydict ={'name': 'John', 'age': 30, 'city': 'New York'} # Creating a dictionary with multiple key-value pairs

print(dict['one'])  # Accessing a value using its key
print(dict[2])      # Accessing another value using its key
print(tinydict.keys) # Accessing all keys in the dictionary
print(tinydict.values()) # Accessing all values in the dictionary
print(tinydict.items())  # Accessing all key-value pairs in the dictionary


